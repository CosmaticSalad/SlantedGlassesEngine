FLAG GameText::PrintScene_GameText(BasicFunctions* bFunctions, uint GTscene, uchar& pInput)
{




    flag needMenu = false;


    switch (GTscene)
    {

    case (uint)0:

        needMenu = gtCase0(bFunctions);

        bFunctions->SetCursor_IncrementY();

        




        break;

    case (uint)1:

        needMenu = gtCase1(bFunctions);


        break;

    case (uint)2:


        needMenu = gtCase2(bFunctions);


        break;

    default:


        break;

    }

    return needMenu;

}


